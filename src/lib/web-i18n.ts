import type { Locale } from "./i18n";

function withLanguage(path: string, locale: Locale): string {
  const [pathname, queryString = ""] = path.split("?");
  const searchParams = new URLSearchParams(queryString);
  searchParams.set("lang", locale);
  const query = searchParams.toString();
  return query ? `${pathname}?${query}` : pathname;
}

export function languageSwitcher(currentPath: string, locale: Locale): string {
  return `
    <div class="button-row" style="justify-content:flex-end; margin-bottom:12px;">
      <a class="button ${locale === "zh-CN" ? "primary" : ""}" href="${withLanguage(currentPath, "zh-CN")}">中文</a>
      <a class="button ${locale === "en" ? "primary" : ""}" href="${withLanguage(currentPath, "en")}">English</a>
    </div>
  `;
}

